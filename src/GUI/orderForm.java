    private void PAYActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PAYActionPerformed
        // TODO add your handling code here:
        
        String Cash = IC.getText();
        if (IC.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,"Input Cash kosong, silahkan isi terlebih dahulu");
        }
        Double InputCash = Double.parseDouble(Cash);
        
        String TPayment = TP.getText();
        Double TotalPayment = Double.parseDouble(TPayment);
        
        
        if ( InputCash > TotalPayment ) {
            Double change = InputCash - TotalPayment;
            String changes = Double.toString(change);
            CHPS.setText("Rp. " + Cash);
            ChangePS.setText("Rp. " + changes);
            
            
            PaymentSuccess.setPreferredSize(new Dimension(1152, 699));
            PaymentSuccess.pack();
            PaymentSuccess.setVisible(true);
        } else if (InputCash < TotalPayment) {
            JOptionPane.showMessageDialog(null,"Jumlah uang yang di input kurang dari Total Payment");
        } else {
            CHPS.setText("Rp. " + Cash);
            ChangePS.setText("Rp. 0");
            PaymentSuccess.setPreferredSize(new Dimension(1152, 699));
            PaymentSuccess.pack();
            PaymentSuccess.setVisible(true);
        }
        PaymentPage.setVisible(false);
        
        
    }//GEN-LAST:event_PAYActionPerformed

