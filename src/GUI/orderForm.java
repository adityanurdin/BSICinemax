    private void CBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CBActionPerformed
        // TODO add your handling code here:
//        this.setVisible(false);
//        new orderForm().setVisible(true);
      
          String CashierName = CN.getText();
          String ChooseFilm  = CF.getSelectedItem().toString();
          String NumberTicket = NOT.getText();
          int tiket = Integer.parseInt(NumberTicket);
          String SeatPosition = SP.getText();
          int HargaTicket = 35000;
          
          if (CN.getText().isEmpty() || NOT.getText().isEmpty() || SP.getText().isEmpty() ) {
              JOptionPane.showMessageDialog(null, "Maaf , Form masih kosong");
          }
          
          Random rand = new Random();
          int n = rand.nextInt(99999);
          
          
          ON.setText("#" + n);
          ONPS.setText("#" + n);
          DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
          LocalDateTime now = LocalDateTime.now();  
          OD.setText(dtf.format(now));
          ODPS.setText(dtf.format(now));
          Cashier.setText(CashierName);
          CPS.setText(CashierName);
          SeatPS.setText(SeatPosition);
          
          SF.setText(ChooseFilm + "(x" + NumberTicket + ")");
          ChoosedFilm.setText(ChooseFilm + "(x" + NumberTicket + ")");
          int sb = HargaTicket * tiket;
          double pajak = sb * 0.1;
          String subtotal = Integer.toString(sb);
          String pajaks = Double.toString(pajak);
          double total = sb + pajak;
          String totals = Double.toString(total);
          
          if (SP.getText().isEmpty()) {
              Paynow.setEnabled(false);
          } 
          
          ST.setText("Rp. " + subtotal);
          SubHarga.setText("Rp. " + subtotal);
          STPS.setText("Rp. " + subtotal);
          PPN.setText("Rp. " +pajaks);
          PPNPS.setText("Rp. " +pajaks);
          TTL.setText("Rp. " + totals);
          TTLPS.setText("Rp. " + totals);
          TP.setText(totals);
    }//GEN-LAST:event_CBActionPerformed

